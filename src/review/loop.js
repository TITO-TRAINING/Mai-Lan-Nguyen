// for loop
for (let i = 0; i <= 5; i++) {
  console.log(i);
}
// 0 1 2 3 4 5

// while loop
let i = 0;
while (i <= 5) {
  console.log(i);
  i++;
}
// 0 1 2 3 4 5

// do while loop
let j = 0;
do {
  console.log(j);
  j++;
} while (j <= 5);

// 0 1 2 3 4 5

// for of loop
const numbers = [1, 2, 3, 4, 5];

for (const num of numbers) {
  console.log(num);
}
// 1 2 3 4 5
